void main() {
  // Datetype
  bool isTrue = true;
  print("Boolean data type :  $isTrue");

  int number = 10;
  print("Int data type : $number");

  String name = "Dipen";
  print("String data type : $name");

  //Keyword
  late int exampleInt;
  exampleInt = 100;
  print("Late Initialized Var : $exampleInt");

  final int finalInt = 2; // Assigned at the run time
  print("Final Int : $finalInt");

  const int constInt = 4;
  print("Const Int : $constInt");

  dynamic dynamicVariable = 2; // Not Feasible
  //dynamicVariable = "Hello"
  print("Dynamic Variable $dynamicVariable");

  var varOutPut = 1.5;
  print("Var dataType : $varOutPut");
}
